/**
 * This file has win32 line-endings. Adds two numbers.
 * @param {number} a The first number to be added.
 * @param {number} b The second number to be added.
 * @returns {number} The sum of a and b.
 * @example
 * let a = 1;
 * let b = 2;
 * // returns 3
 * add(a, b);
 */
function add(a, b) {
    return a + b;
}
